package javasessions;

public class Loops {

	public static void main(String[] args) {
		
		
		int i=1;
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}
		
		
		
		//for loop
		int k=1;
				for(; k<=10;k++ )
				{
					System.out.println(k);
				}
				
				//for each
				
				int ar[] = { 10, 50, 60, 80, 90 }; 
				  
		        for (int element : ar) 
		  
		        {
		            System.out.print(element + " ");
		        }
				
		
	}

}
